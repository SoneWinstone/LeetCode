#include <iostream>
#include <vector>
#include <unordered_map>

using namespace std;

//leetcode submit region begin(Prohibit modification and deletion)
class Solution {
public:
    vector<int> findAnagrams(string s2, string s1) {
        vector<int> ret{};

        unordered_map<char, int> window{};
        unordered_map<char, int> need{};
        for (char c : s1) {
            need[c]++;
        }

        int left = 0, right = 0;
        int valid = 0;
        while (right < s2.size()) {
            char c = s2[right];
            auto it = need.find(c);
            if (it != need.end()) {
                window[c]++;
                if (window[c] == need[c]) {
                    valid++;
                }
            }
            ++right;

            while (right - left >= s1.size()) {
                if (valid == need.size()) {
                    ret.push_back(left);
                }

                char lc = s2[left];
                auto lit = need.find(lc);
                if (lit != need.end()) {
                    if (window[lc] == need[lc]) {
                        valid--;
                    }
                    window[lc]--;
                }
                ++left;
            }
        }
        return ret;
    }
};
//leetcode submit region end(Prohibit modification and deletion)

int main() {
    string s1 = "abab";
    string s2 = "ab";
    Solution solution;
    auto vec = solution.findAnagrams(s1, s2);

    cout << "[";
    for (auto it = vec.begin(); it != vec.end(); ++it) {
        cout << *it << ",";
    }
    cout << "\b]" << endl;

    return 0;
}